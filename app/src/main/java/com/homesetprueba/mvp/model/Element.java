package com.homesetprueba.mvp.model;


public class Element {

    private int id;
    private String image;
    private String title;
    private String category;

    public int getId() {
        return id;
    }

    public String getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

    public String getCategory() {
        return category;
    }
}
